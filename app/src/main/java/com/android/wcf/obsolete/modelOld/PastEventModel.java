public class PastEventModel {

    String eventImageLink = "";
    String eventName = "";
    String eventMonthYear = "";
    String eventTeamName = "";
    String eventMoney = "";
    String eventMiles = "";

    public String getEventImageLink() {
        return eventImageLink;
    }

    public void setEventImageLink(String eventImageLink) {
        this.eventImageLink = eventImageLink;
    }

    public String getEventName() {
        return eventName;
    }

    public void setEventName(String eventName) {
        this.eventName = eventName;
    }

    public String getEventMonthYear() {
        return eventMonthYear;
    }

    public void setEventMonthYear(String eventMonthYear) {
        this.eventMonthYear = eventMonthYear;
    }

    public String getEventTeamName() {
        return eventTeamName;
    }

    public void setEventTeamName(String eventTeamName) {
        this.eventTeamName = eventTeamName;
    }

    public String getEventMoney() {
        return eventMoney;
    }

    public void setEventMoney(String eventMoney) {
        this.eventMoney = eventMoney;
    }

    public String getEventMiles() {
        return eventMiles;
    }

    public void setEventMiles(String eventMiles) {
        this.eventMiles = eventMiles;
    }
}
